# ¿Por qué programación orientada a objetos?

# La verdad es que con los tipos primitivos podemos hacer CUALQUIER programa o aplicacioón.

# ¿Entonces por qué objetos?

# Respuesta: Para gestionar la complejidad (especialmente para programas largos)

# La programación orientada a objetos permite crear aplicaciones utilizando métodos sobre objetos y no operaciones con  primitivos, lo que permite un mejor manejejo de la complejidad y mantenibilidad de sistemas grandes.

# Al momento de pensar los objetos que vamos a necesitar en nuestros programas, notaremos similitudes entre ellos, lo que nos llevará al concepto de Clases